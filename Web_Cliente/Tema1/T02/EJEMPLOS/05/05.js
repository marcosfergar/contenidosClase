let dia = new Date().getDay();


switch (dia) {
 case 1:
    return console.log("Hoy es lunes");
 break;
 case 2:
    return console.log("Hoy es martes");
 break;
 case 3:
    return console.log("Hoy es Miercoles");
 break;
 case 4:
    return console.log("Hoy es Jueves");
 break;
 case 5:
    return console.log("Hoy es viernes");
 break;

}

